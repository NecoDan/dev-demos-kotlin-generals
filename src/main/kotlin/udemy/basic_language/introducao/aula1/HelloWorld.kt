fun main(args: Array<String>) {
    println("Iniciando a primeira aula de Kotlin Udemy - Aprenda o básico da linguagem Kotlin")

    // Nao pode ser alterada em tempo de execução, funciona como uma constante....
    val PI = 3.14
    println("Valor de PI eh igual a =  $PI")

